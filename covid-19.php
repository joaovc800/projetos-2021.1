                <h2 class="blog-post-title">1 Ano sem encontrar o tratamento de COVID-19</h2>
                <p class="blog-post-meta">Março 13, 2021</p>

                <p>Um ano atrás a OMS declarou que a COVID-19 passou ao status de pandemia. Desde então, cientistas ao redor do mundo estão em busca de um tratamento específico para essa doença, sem muito sucesso até o momento.</p>
                <hr>
                <p>No começo, a principal estratégia de busca utilizada foi o reposicionamento de fármacos. Assim, essa alternativa utiliza remédios já aprovados para uso em certas doenças, e tenta tratar novas doenças com o mesmo remédio. Por serem remédios conhecidos, já existem diversos estudos de como agem no corpo, e quais doses são seguras de serem tomadas. Dessa forma, eles necessitam de menos investimento de tempo e dinheiro para as pesquisas com as novas doenças, como a COVID-19. Contudo, a “pressa” para encontrar uma resposta contra a pandemia provocou a disseminação de algumas informações equivocadas que acabaram sendo adotadas como verdade absoluta por parte da população.</p>
                <blockquote>
                  <p>Boa parte dos experimentos iniciais realizados com esses remédios foram feitos em um sistema in vitro. Ou seja, isso significa que os cientistas cultivaram células em pequenas placas, e fizeram os testes. Em situações normais, depois que os testes in vitro são feitos, os remédios acabam em uma etapa de testes em animais. Esses testes se fazem necessários. Pois, diferente de um sistema in vivo, ou seja, em um animal, os experimentos em culturas de células são muito controlados e não representam toda a complexidade que um corpo possui. Assim, muitas coisas podem apresentar resultados promissores nos testes com células, mas falham quando testados para os animais.<br><br>

                  No contexto da Pandemia, esses remédios de testes de reposicionamento podem pular a etapa de segurança e acabam nos estudos clínicos com humanos. Afinal já conhecia-se a relação de segurança do remédio. Só não sabíamos se ele funcionava para COVID-19. Dois grandes estudos clínicos foram realizados, o SOLIDARITY, organizado pela OMS e o RECOVERY, organizado pela Universidade de Oxford. Entretanto, foram nesses estudos clínicos, realizados com milhares de pessoas, que os resultados mostraram que a maioria dos remédios falharam.</p>
                </blockquote>
                <h2>Um pouco de calma, antes de sair tomando remédio nunca fez mal a ninguém…</h2>
                <p>Mas por que não podemos tomar esses remédios, ao passo que eles constam na bula como seguros, mesmo sem um efeito comprovado? Primeiro que ser seguro, não quer dizer “de qualquer jeito” e “tomando enquanto eu estiver com vontade ou a pandemia durar” (o que acabar primeiro.</p>
                <p>Segundo, que mesmo aqueles aprovados e vendidos em farmácias possuem uma dose e um tempo certo de administração. Por exemplo, você já reparou que todo remédio possui uma bula que relata efeitos adversos que podem ser simples, ou até mesmo muito graves?</p>
                <p>Já existem relatos de pessoas que tiveram problemas de saúde decorrente do uso profilático de remédios sem prescrição médica, como por exemplo <strong>ivermectina e cloroquina.</strong> Em uma entrevista dada ao Jornal “O Globo”, o médico hepatologista Paulo Bittencourt informou que 27% das hepatites agudas graves ou fulminantes são de origem de medicamentos.</p>
                <p>Além disso, outros problemas podem surgir, como por exemplo a falta desses remédios para quem realmente precisa tomar. Assim, cria-se uma falsa sensação de segurança, e as pessoas param de adotar medidas que realmente funcionam para o enfrentamento da pandemia, como uso de máscaras e distanciamento social. E também, investir em um remédio que não funciona significa que o dinheiro disponível para o combate a pandemia está sendo mal gasto.</p>
                <p>Abaixo, preparamos uma lista com as principais tentativas de tratamento utilizadas durante esse primeiro ano de pandemia.</p>
                <ul>
                  <li>Hidroxicloroquina e Cloroquina.</li>
                  <li>Ivermectina.</li>
                  <li>Remdesivir.</li>
                </ul>
                <p>Ainda temos mais esses 3 tópicos.</p>
                <ol>
                  <li>Lopinavir e Ritonavir.</li>
                  <li>Azitromicina.</li>
                  <li>Dexametasona.</li>
                </ol>
                <p>Seguimos adiante com mais informações...</p>
